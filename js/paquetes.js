    function aumentartamañoTarjeta(tarjeta) {
        tarjeta.style.transform = "scale(1.05)"; // Hace que la tarjeta se agrande en un 5%
        tarjeta.style.backgroundColor = "#fff"; // Cambia el color de fondo al azul cuando se pasa el mouse
    }

    function RegresarTamañoNormal(tarjeta) {
        tarjeta.style.transform = "scale(1)"; // Restaura el tamaño original de la tarjeta
        tarjeta.style.backgroundColor = "#fff"; // Restaura el color de fondo original
    }



    

  
        
        
 
    